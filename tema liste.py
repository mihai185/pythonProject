my_list = [7, 8, 9, 2, 3, 1, 4, 10, 5, 6]
print(my_list)

my_list.sort()
print(my_list)

my_list.reverse()
print(my_list)

my_list.sort()
my_sliced_list = my_list[1::2]
print(my_sliced_list)

my_list.sort()
my_sliced_list = my_list[::2]
print(my_sliced_list)

my_list.sort()
my_sliced_list = my_list[2:9:3]
print(my_sliced_list)

